const LanguageManager = {
  init() {
    this.langToggleBtn = document.getElementById('lang-toggle');
    this.currentLang = localStorage.getItem('readora_lang') || 'ar';

    this.applyLanguage(this.currentLang);

    if (this.langToggleBtn) {
      this.langToggleBtn.addEventListener('click', () => this.toggleLanguage());
    }
  },

  applyLanguage(lang) {
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    localStorage.setItem('readora_lang', lang);
    this.currentLang = lang;

    if (this.langToggleBtn) {
      const textSpan = this.langToggleBtn.querySelector('span');
      if (textSpan) {
        textSpan.textContent = lang === 'ar' ? 'EN' : 'عربي';
      }
    }

    this.translatePage();
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));
  },

  toggleLanguage() {
    const newLang = this.currentLang === 'ar' ? 'en' : 'ar';
    this.applyLanguage(newLang);
  },

  translatePage() {
    const elements = document.querySelectorAll('[data-i18n], [data-i18n-placeholder], [data-i18n-title]');
    elements.forEach((el) => {
      const key = el.getAttribute('data-i18n');
      const placeholderKey = el.getAttribute('data-i18n-placeholder');
      const titleKey = el.getAttribute('data-i18n-title');
      const currentTranslations = translations[this.currentLang] || translations.ar;

      if (placeholderKey && currentTranslations[placeholderKey]) {
        el.placeholder = currentTranslations[placeholderKey];
      }

      if (titleKey && currentTranslations[titleKey]) {
        el.setAttribute('title', currentTranslations[titleKey]);
        el.setAttribute('aria-label', currentTranslations[titleKey]);
      }

      if (key && currentTranslations[key]) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = currentTranslations[key];
        } else {
          el.textContent = currentTranslations[key];
        }
      }
    });
  },

  get(key) {
    return translations[this.currentLang][key] || key;
  }
};

document.addEventListener('DOMContentLoaded', () => {
  LanguageManager.init();
});
